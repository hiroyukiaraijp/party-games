// Sort by kanji length descending for greedy matching
const _sortedDict = _RUBY_DICT.slice().sort((a, b) => b[0].length - a[0].length);

function _escH(s) {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function addRuby(text) {
  if (!text) return '';
  let result = '';
  let i = 0;
  while (i < text.length) {
    let matched = false;
    for (const [kanji, reading] of _sortedDict) {
      if (text.startsWith(kanji, i)) {
        result += `<ruby>${_escH(kanji)}<rt>${reading}</rt></ruby>`;
        i += kanji.length;
        matched = true;
        break;
      }
    }
    if (!matched) {
      result += _escH(text[i]);
      i++;
    }
  }
  return result;
}

function isRubyEnabled() {
  return localStorage.getItem(RUBY_ENABLED_KEY) === 'on';
}

// Apply ruby to all elements with data-ruby attribute
function applyRuby() {
  if (!isRubyEnabled()) return;
  document.querySelectorAll('[data-ruby]').forEach(el => {
    if (el._rubyApplied) return;
    el._rubyApplied = true;
    el._originalText = el.textContent;
    el.innerHTML = addRuby(el._originalText);
  });
}

// Remove ruby from all elements
function removeRuby() {
  document.querySelectorAll('[data-ruby]').forEach(el => {
    if (!el._rubyApplied) return;
    el._rubyApplied = false;
    el.textContent = el._originalText || el.textContent;
  });
}

// For dynamically rendered content: call this with the text to get ruby HTML if enabled
function rubyText(text) {
  return isRubyEnabled() ? addRuby(text) : _escH(text);
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => {
  applyRuby();
});
